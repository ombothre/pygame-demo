# Conditionals in Python

# If-else statement
age = 18
if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# Elif statement
score = 85
if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")
elif score >= 70:
    print("Grade: C")
else:
    print("Grade: F")
